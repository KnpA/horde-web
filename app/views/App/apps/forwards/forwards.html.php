<p>Forwards is a Horde module for setting user e-mail forwards with support
for several popular mailers.</p>

<p>Right now, Forwards provides fairly complete support for setting .forward
style forwards on Sendmail, Courier, or Qmail mail based systems via an FTP
transport. It now also has drivers for Mdaemon, Exim SQL, Exim LDAP, Custom
SQL, and SOAP based systems.</p>

<p>Forwards is part of a suite of account management modules for Horde
consisting of Forwards, <?php echo $this->linkTo('Passwd', array('controller'
