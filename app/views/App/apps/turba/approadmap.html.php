<h3>Turba Roadmaps</h3>
<p>
The following roadmaps summarize the important changes planned for
future releases.  They do not list all changes, only changes that
are considered significant from the end user's point of view.
In addition to the changes listed, most releases will probably contain
at least some of the following changes:
</p>
<ul>
  <li>Bug fixes, performance tweaks, code cleanups, etc.</li>
  <li>New and/or improved translations.</li>
  <li>New preferences or configuration options.</li>
</ul>

