<h3>Related Downloads</h3>

<p>Plugins are also available for uploading pictures directly to Ansel from both
Aperture and iPhoto. Both Ansel versions 1.x and 2.x are supported by these
uploaders.</p>

<ul>
 <li><a href="ftp://ftp.horde.org/pub/iPhoto2Ansel/iPhoto2Ansel-1.0.0alpha1.pkg">
 iPhoto Plugin</a> Requires OS X 10.5 or later, has been tested with iPhoto 08 and 09.</li>
 <li><a href="ftp://ftp.horde.org/pub/ApertureToAnselExportPlugin/ApertureToAnsel-1.0.0alpha1.pkg">
 Aperture Plugin</a> Requires OS x 10.5 or later, tested with Aperture 2.1 through 3.2.1.</li>
</ul>
